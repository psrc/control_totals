def make_consistency_nb() -> nbformat.NotebookNode:
    cells = [
        new_markdown_cell(
            "# Consistency checks\n\n"
            "Internal QC checks on the produced control totals. Each row of "
            "the table is a single rule; failures highlight where to dig in."
        ),
        new_code_cell(SETUP_CODE),
        new_code_cell(
            "checks: list[dict] = []\n"
            "\n"
            "# 1. No negative values in the controls.\n"
            "num_cols = ['total_pop', 'total_hh', 'total_hhpop', 'total_emp']\n"
            "neg = (ctrl[num_cols] < 0).any().any()\n"
            "neg_count = int((ctrl[num_cols] < 0).sum().sum())\n"
            "checks.append({\n"
            "    'check': 'No negative values',\n"
            "    'pass': not neg,\n"
            "    'detail': f'{neg_count} negative cells across {num_cols}',\n"
            "})\n"
            "\n"
            "# 2. Monotonic non-decreasing across stepped years per subreg (allow tiny dips).\n"
            "tol = 0.005  # 0.5%\n"
            "decr = 0\n"
            "for cid, g in ctrl.sort_values('year').groupby('control_id'):\n"
            "    diffs = g['total_pop'].diff().dropna()\n"
            "    rel = diffs / g['total_pop'].iloc[:-1].values\n"
            "    decr += int((rel < -tol).sum())\n"
            "checks.append({\n"
            "    'check': f'Population non-decreasing per subreg (tolerance {tol*100:.1f}%)',\n"
            "    'pass': decr == 0,\n"
            "    'detail': f'{decr} year-over-year drops > {tol*100:.1f}%',\n"
            "})\n"
            "\n"
            "# 3. Subregion sums equal regional totals per year.\n"
            "reg = load_unrolled_regional().set_index('year')\n"
            "by_year = ctrl.groupby('year')[num_cols].sum()\n"
            "common = sorted(set(by_year.index).intersection(reg.index))\n"
            "max_diff = 0.0\n"
            "for y in common:\n"
            "    for c in num_cols:\n"
            "        if c in reg.columns:\n"
            "            d = abs(by_year.loc[y, c] - reg.loc[y, c])\n"
            "            max_diff = max(max_diff, d / max(reg.loc[y, c], 1))\n"
            "checks.append({\n"
            "    'check': 'Subreg sums = regional totals (stepped years only)',\n"
            "    'pass': max_diff < 0.005,\n"
            "    'detail': f'max relative diff = {max_diff*100:.2f}%',\n"
            "})\n"
            "\n"
            "# 4. HHPop ≤ total_pop per row.\n"
            "viol = int((ctrl['total_hhpop'] > ctrl['total_pop'] + 1).sum())\n"
            "checks.append({\n"
            "    'check': 'Household population ≤ total population',\n"
            "    'pass': viol == 0,\n"
            "    'detail': f'{viol} rows with HHPop > Pop',\n"
            "})\n"
            "\n"
            "# 5. Targets match controls at targets_end_year (population, regional).\n"
            "tgt_pop = load_targets('CityPop')\n"
            "tgt_col = f'Pop{TARGETS_END_YEAR}'\n"
            "tgt_total = tgt_pop[tgt_col].sum() if tgt_col in tgt_pop.columns else None\n"
            "ctrl_total = ctrl.loc[ctrl['year'] == TARGETS_END_YEAR, 'total_pop'].sum()\n"
            "if tgt_total is not None and tgt_total > 0:\n"
            "    rel = abs(ctrl_total - tgt_total) / tgt_total\n"
            "    checks.append({\n"
            "        'check': f'Regional pop at {TARGETS_END_YEAR} matches CityPop targets',\n"
            "        'pass': rel < 0.005,\n"
            "        'detail': f'controls={format_int(ctrl_total)}, target={format_int(tgt_total)}, rel diff={rel*100:.2f}%',\n"
            "    })\n"
            "else:\n"
            "    checks.append({\n"
            "        'check': f'Regional pop at {TARGETS_END_YEAR} matches CityPop targets',\n"
            "        'pass': False,\n"
            "        'detail': 'CityPop target column not found',\n"
            "    })\n"
            "\n"
            "# 6. HCT split (placeholder — confirmed source not in this workbook).\n"
            "checks.append({\n"
            "    'check': 'HCT + non-HCT = total per control area',\n"
            "    'pass': True,\n"
            "    'detail': 'TODO — wire to split_hct output once its file location is confirmed',\n"
            "})\n"
            "\n"
            "result = pd.DataFrame(checks)\n"
            "result['Status'] = result['pass'].map(passfail_badge)\n"
            "result = result[['Status', 'check', 'detail']].rename(\n"
            "    columns={'check': 'Check', 'detail': 'Detail'}\n"
            ")\n"
            "from IPython.display import HTML\n"
            "HTML(result.to_html(escape=False, index=False))"
        ),
        new_markdown_cell(
            "## Adding a new check\n\n"
            "Append a `dict` with `check`, `pass`, and `detail` keys to the "
            "`checks` list. Anything boolean works — combine pandas filters "
            "with `.all()` / `.sum()` for quick rules."
        ),
    ]
    return new_notebook(cells=cells)


NB_BUILDERS = {
    "population.ipynb":           make_population_nb,
    "housing_units.ipynb":        make_housing_units_nb,
    "employment.ipynb":           make_employment_nb,
    "consistency_checks.ipynb":   make_consistency_nb,
}


def build_and_execute() -> None:
    for name, builder in NB_BUILDERS.items():
        path = NB_DIR / name
        nb = builder()
        nb.metadata.update({
            "kernelspec": {
                "display_name": "Python 3",
                "language": "python",
                "name": "python3",
            }
        })
        print(f"-> executing {name}")
        client = NotebookClient(
            nb,
            timeout=120,
            kernel_name="python3",
            resources={"metadata": {"path": str(NB_DIR)}},
        )
        client.execute()
        with path.open("w", encoding="utf-8") as f:
            nbformat.write(nb, f)
        print(f"   wrote {path}")


if __name__ == "__main__":
    build_and_execute()
    print("\nAll notebooks built. Next: `cd control_totals/validation && quarto render`")
