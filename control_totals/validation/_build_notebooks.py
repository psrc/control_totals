"""One-off generator for the validation-dashboard example notebooks.

Run from the repo root:
    .venv\\Scripts\\python.exe control_totals\\validation\\_build_notebooks.py

This produces (and executes) four example .ipynb files in
control_totals/validation/summary_scripts/. Re-run any time the example
notebooks need to be regenerated from scratch.

Hand-edited notebooks should NOT be regenerated by this script — instead
add new chapters by copying one of the produced notebooks and editing it
in JupyterLab / VS Code.
"""

from __future__ import annotations

import sys
from pathlib import Path

import nbformat
from nbformat.v4 import new_code_cell, new_markdown_cell, new_notebook
from nbclient import NotebookClient

HERE = Path(__file__).resolve().parent
NB_DIR = HERE / "summary_scripts"
NB_DIR.mkdir(parents=True, exist_ok=True)


# Boilerplate prepended to every notebook so cells can import the helpers
# without needing the user to install the validation folder as a package.
SETUP_CODE = """\
import sys
from pathlib import Path

# Make summary_scripts/ importable regardless of where the kernel started.
_HERE = Path.cwd()
for candidate in [_HERE, _HERE / "summary_scripts", _HERE.parent / "summary_scripts"]:
    if (candidate / "validation_data_input.py").exists():
        sys.path.insert(0, str(candidate))
        break

import pandas as pd
import plotly.express as px
import plotly.graph_objects as go

from validation_data_input import (
    load_config, load_settings, load_unrolled, load_unrolled_regional,
    load_targets, control_id_lookup, load_legacy_pop, load_legacy_units,
    available_years,
)
from util import (
    COUNTY_ID_TO_NAME, COUNTY_ORDER, COUNTY_COLORS, INDICATORS,
    apply_plotly_theme, format_int, pct_diff, style_diff_table, passfail_badge,
)

cfg = load_config()
settings = load_settings()
BASE_YEAR = settings.get("base_year")
END_YEAR = settings.get("end_year")
TARGETS_END_YEAR = settings.get("targets_end_year")

ctrl = load_unrolled().merge(control_id_lookup(), on="control_id", how="left")
ctrl["county"] = ctrl["county_id"].map(COUNTY_ID_TO_NAME)
"""


def make_population_nb() -> nbformat.NotebookNode:
    cells = [
        new_markdown_cell(
            "# Population\n\n"
            "Total population by county across stepped years, compared to the "
            "regional totals and to legacy LUVit estimates when available."
        ),
        new_code_cell(SETUP_CODE),
        new_markdown_cell("## County totals at base year and end year"),
        new_code_cell(
            "by_county_year = (\n"
            "    ctrl.groupby(['county', 'year'])['total_pop'].sum().unstack('year')\n"
            ")\n"
            "by_county_year = by_county_year.reindex(COUNTY_ORDER)\n"
            "by_county_year['Region'] = by_county_year.sum(axis=0)  # column-wise won't help\n"
            "by_county_year = by_county_year.drop(columns='Region')\n"
            "summary = pd.DataFrame({\n"
            "    f'{BASE_YEAR}': by_county_year[BASE_YEAR],\n"
            "    f'{TARGETS_END_YEAR}': by_county_year[TARGETS_END_YEAR],\n"
            "    f'{END_YEAR}': by_county_year[END_YEAR],\n"
            "    'Growth (base→targets)': by_county_year[TARGETS_END_YEAR] - by_county_year[BASE_YEAR],\n"
            "    '% Growth': (by_county_year[TARGETS_END_YEAR] / by_county_year[BASE_YEAR] - 1) * 100,\n"
            "})\n"
            "summary.loc['Region'] = summary.sum(numeric_only=True)\n"
            "summary.loc['Region', '% Growth'] = (\n"
            "    summary.loc['Region', f'{TARGETS_END_YEAR}']\n"
            "    / summary.loc['Region', f'{BASE_YEAR}'] - 1\n"
            ") * 100\n"
            "style_diff_table(\n"
            "    summary,\n"
            "    int_cols=[f'{BASE_YEAR}', f'{TARGETS_END_YEAR}', f'{END_YEAR}', 'Growth (base→targets)'],\n"
            "    pct_cols=['% Growth'], pct_threshold=100,\n"
            ")"
        ),
        new_markdown_cell("## Trajectory by county"),
        new_code_cell(
            "trend = ctrl.groupby(['county', 'year'])['total_pop'].sum().reset_index()\n"
            "fig = px.line(\n"
            "    trend, x='year', y='total_pop', color='county',\n"
            "    category_orders={'county': COUNTY_ORDER},\n"
            "    color_discrete_map=COUNTY_COLORS, markers=True,\n"
            "    labels={'total_pop': 'Population', 'year': 'Year', 'county': 'County'},\n"
            "    title='Total population by county',\n"
            ")\n"
            "apply_plotly_theme(fig)"
        ),
        new_markdown_cell("## Regional total — controls vs. legacy LUVit"),
        new_code_cell(
            "reg = load_unrolled_regional()[['year', 'total_pop']].copy()\n"
            "reg['source'] = 'New pipeline'\n"
            "legacy = load_legacy_pop()\n"
            "frames = [reg]\n"
            "if legacy is not None:\n"
            "    leg = legacy.rename(columns={c: c.lower() for c in legacy.columns})\n"
            "    if 'year' in leg.columns and 'total_pop' in leg.columns:\n"
            "        leg = leg[['year', 'total_pop']].copy()\n"
            "        leg['source'] = 'Legacy LUVit'\n"
            "        frames.append(leg)\n"
            "compare = pd.concat(frames, ignore_index=True)\n"
            "fig = px.line(\n"
            "    compare, x='year', y='total_pop', color='source', markers=True,\n"
            "    title='Regional population — pipeline vs. legacy',\n"
            "    labels={'total_pop': 'Population', 'year': 'Year', 'source': 'Source'},\n"
            ")\n"
            "if legacy is None:\n"
            "    fig.add_annotation(\n"
            "        text='Legacy LUVit estimates not available for this run.',\n"
            "        xref='paper', yref='paper', x=0.5, y=1.08,\n"
            "        showarrow=False, font=dict(color='#888'),\n"
            "    )\n"
            "apply_plotly_theme(fig)"
        ),
        new_markdown_cell(
            "## Notes\n\n"
            "- Base year, targets year, and end year come from "
            "`configs/settings.yaml` of the active example.\n"
            "- Legacy comparison loads "
            "`examples/legacy_luvit/output/total_pop_estimates_all_years.csv` "
            "if present and non-empty."
        ),
    ]
    return new_notebook(cells=cells)


def make_housing_units_nb() -> nbformat.NotebookNode:
    cells = [
        new_markdown_cell(
            "# Housing units\n\n"
            "Household totals by county and growth from base year to targets "
            "year — and the implied delta vs. the input city-level targets."
        ),
        new_code_cell(SETUP_CODE),
        new_markdown_cell("## Households by county"),
        new_code_cell(
            "hh = ctrl.groupby(['county', 'year'])['total_hh'].sum().unstack('year')\n"
            "hh = hh.reindex(COUNTY_ORDER)\n"
            "summary = pd.DataFrame({\n"
            "    f'HH {BASE_YEAR}': hh[BASE_YEAR],\n"
            "    f'HH {TARGETS_END_YEAR}': hh[TARGETS_END_YEAR],\n"
            "    'Growth': hh[TARGETS_END_YEAR] - hh[BASE_YEAR],\n"
            "    '% Growth': (hh[TARGETS_END_YEAR] / hh[BASE_YEAR] - 1) * 100,\n"
            "})\n"
            "summary.loc['Region'] = summary.sum(numeric_only=True)\n"
            "summary.loc['Region', '% Growth'] = (\n"
            "    summary.loc['Region', f'HH {TARGETS_END_YEAR}']\n"
            "    / summary.loc['Region', f'HH {BASE_YEAR}'] - 1\n"
            ") * 100\n"
            "style_diff_table(\n"
            "    summary,\n"
            "    int_cols=[f'HH {BASE_YEAR}', f'HH {TARGETS_END_YEAR}', 'Growth'],\n"
            "    pct_cols=['% Growth'], pct_threshold=100,\n"
            ")"
        ),
        new_markdown_cell("## Growth (base → targets) by county"),
        new_code_cell(
            "growth = (hh[TARGETS_END_YEAR] - hh[BASE_YEAR]).reset_index()\n"
            "growth.columns = ['county', 'growth']\n"
            "fig = px.bar(\n"
            "    growth, x='county', y='growth', color='county',\n"
            "    category_orders={'county': COUNTY_ORDER},\n"
            "    color_discrete_map=COUNTY_COLORS,\n"
            "    title=f'Household growth, {BASE_YEAR}→{TARGETS_END_YEAR}',\n"
            "    labels={'growth': 'Households added', 'county': 'County'},\n"
            ")\n"
            "fig.update_layout(showlegend=False)\n"
            "apply_plotly_theme(fig)"
        ),
        new_markdown_cell("## Output vs. input city targets (CityHH)"),
        new_code_cell(
            "targets = load_targets('CityHH')\n"
            "# CityHH columns: RGID, county_id, control_id, Juris, HH<base_year>, HH<base_year>+5..., HHGro<base..targets>, HH<end_year>\n"
            "base_col = f'HH{BASE_YEAR}'\n"
            "# growth column name is e.g. HHGro2350 when base=2023, targets_end=2050\n"
            "gro_col = next(c for c in targets.columns if c.startswith('HHGro'))\n"
            "ctrl_end = (\n"
            "    ctrl.loc[ctrl['year'] == TARGETS_END_YEAR, ['control_id', 'total_hh']]\n"
            "    .rename(columns={'total_hh': 'HH_end_ctrl'})\n"
            ")\n"
            "compare = targets.merge(ctrl_end, on='control_id', how='left')\n"
            "compare['county'] = compare['county_id'].map(COUNTY_ID_TO_NAME)\n"
            "by_co = compare.groupby('county').agg(\n"
            "    target=(gro_col, 'sum'),\n"
            "    base=(base_col, 'sum'),\n"
            "    end=('HH_end_ctrl', 'sum'),\n"
            ")\n"
            "by_co = by_co.reindex(COUNTY_ORDER)\n"
            "by_co['ctrl_growth'] = by_co['end'] - by_co['base']\n"
            "by_co['delta'] = by_co['ctrl_growth'] - by_co['target']\n"
            "by_co['% diff vs. target'] = by_co.apply(\n"
            "    lambda r: pct_diff(r['ctrl_growth'], r['target']), axis=1\n"
            ")\n"
            "by_co = by_co.rename(columns={\n"
            "    'target': 'Target growth',\n"
            "    'ctrl_growth': 'Controls growth',\n"
            "    'delta': 'Controls − Target',\n"
            "})[['Target growth', 'Controls growth', 'Controls − Target', '% diff vs. target']]\n"
            "style_diff_table(\n"
            "    by_co,\n"
            "    int_cols=['Target growth', 'Controls growth', 'Controls − Target'],\n"
            "    pct_cols=['% diff vs. target'], pct_threshold=2,\n"
            ")"
        ),
        new_markdown_cell(
            "## Notes\n\n"
            "- `HHGro2350` is the input target growth (`base_year` → "
            "`targets_end_year`) from `TargetsRebasedOutput.xlsx → CityHH`.\n"
            "- A small percent difference is expected from rounding and from "
            "any RG-level scaling applied during interpolation."
        ),
    ]
    return new_notebook(cells=cells)


